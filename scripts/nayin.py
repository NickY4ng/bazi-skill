def get_nayin(stem, branch):
    """查单柱纳音"""
    key = stem + branch
    return NAYIN_MAP.get(key, "未知")

def get_all_nayin(year_stem, year_branch, month_stem, month_branch, day_stem, day_branch, hour_stem, hour_branch):
    """查四柱纳音"""
    return {
        "年柱": get_nayin(year_stem, year_branch),
        "月柱": get_nayin(month_stem, month_branch),
        "日柱": get_nayin(day_stem, day_branch),
        "时柱": get_nayin(hour_stem, hour_branch),
    }

